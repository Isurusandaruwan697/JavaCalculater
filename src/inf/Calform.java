   public void calculator(){
       value2=Integer.parseInt(jLabel1.getText());
       if("+".equals(operator)){
           result=value1+value2;
           jLabel1.setText(Integer.toString(result));
           
       }   
       else if("-".equals(operator)){
           result=value1-value2;
           jLabel1.setText(Integer.toString(result));
       }
      else if("/".equals(operator)){
           result=value1/value2;
           jLabel1.setText(Integer.toString(result));
       }
      else if("*".equals(operator)){
           result=value1*value2;
           jLabel1.setText(Integer.toString(result));
       }
       
        jLabel2.setText("");

   }
   
